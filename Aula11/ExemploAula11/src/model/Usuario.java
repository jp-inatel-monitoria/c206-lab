package model;

import java.io.Serializable;

/**
 *
 * @author joao_
 */
public class Usuario implements Serializable{
    
    private String email;
    private String senha;

    public Usuario(String email, String senha) {
        this.email = email;
        this.senha = senha;
    }

    public String getEmail() {
        return email;
    }

    public String getSenha() {
        return senha;
    }
 
}
